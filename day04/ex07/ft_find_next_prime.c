int	ft_find_next_prime(int nb)
{
	int i;

	i = 2;
	while (i <= (nb / 2))
	{
		if (nb % i == 0)
		{
			i = 2;
			nb++;
		}
		i++;
	}
	if (nb <= 1)
		return (2);
	else
		return (nb);
}
