#include "extras.h"

char	*ft_strcat(char *dest, char str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	dest[i] = str;
	i++;
	dest[i] = '\0';
	return (dest);
}
